"""
4. Escrever um algoritmo para calcular a média de 4 notas.
"""

grade1 = float(input("Digite sua nota: "))
grade2 = float(input("Digite sua nota: "))
grade3 = float(input("Digite sua nota: "))
grade4 = float(input("Digite sua nota: "))

average = (grade1 + grade2 + grade3 + grade4)/4

print (f"Sua média é: {average:.1f}")